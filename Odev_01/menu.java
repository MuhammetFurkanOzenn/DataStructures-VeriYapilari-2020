    private void jbtnDosyaOkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDosyaOkuActionPerformed
        DosyaSil("kalanMusteri.txt");
        DosyaSil("gunlukIslem.txt");
        JFileChooser chooser = new JFileChooser();
        Scanner in = null;
        String sonucAd = null, sonucOncelik = null, sonucSure = null;
        int sayac = 0, satirAtlamaSayaci = 0;

        if (chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            File selectedFile = chooser.getSelectedFile();
            try {
                in = new Scanner(selectedFile);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(menu.class.getName()).log(Level.SEVERE, null, ex);
            }
            int lineNumber = 1;
            final int donguSayac = 50;
            while (in.hasNextLine() && lineNumber <= donguSayac) {
                //result ="";
                String line = in.nextLine();
                //System.out.println(lineNumber + " + " + line);
                lineNumber++;

                String[] arrSplit = line.split("#");
                sayac = 0;
                for (String arrSplit1 : arrSplit) {
                    //result+=arrSplit1;
                    //System.out.println(arrSplit1);
                    if (sayac == 0) {                                           //isim, öncelik ve süreyi parçalara ayırıyoruz.
                        sonucAd = arrSplit1;
                    } else if (sayac == 1) {
                        sonucOncelik = arrSplit1;
                    } else {
                        sonucSure = arrSplit1;
                    }
                    sayac++;
                }
                System.out.println("Sonuc Ad : " + sonucAd + " Sonuc Sure: " + sonucSure + " Sonuc Oncelik: " + sonucOncelik);

                kuyruk.addLast(sonucAd, Integer.parseInt(sonucSure), Integer.parseInt(sonucOncelik));

//                if (satirAtlamaSayaci++%5 == 4)             // Anlik müşteri gösterilen (no :2) textArea'da 
//                    jtxtAreaUst.append("\n");               // 5 Müşteri yazildiktan sonra alt satıra geçilecek
            }
            jtxtAreaUst.setText(kuyruk.printUst());

            if (in.hasNextLine()) {
                System.out.println("...");
            }
        }
        kuyruk.print();
    }//GEN-LAST:event_jbtnDosyaOkuActionPerformed

