    private void jbtnVezneCalisiyorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnVezneCalisiyorActionPerformed

        //jtxtAreaMusteriGecmis.setText("");
        isWorking = true;
        toplamSure_0 = toplamSure_1 = toplamSure_2 = 0;
        DosyaOlustur("gunlukIslem.txt");

        Thread t = new Thread() {
            @Override
            public void run() {
                try {

                    Thread.sleep(0000);

                    while (isWorking == true && isDone == false) {

                        //-------------------
                        System.out.println("Vezne Calisiyor...");
                        jtxtAreaUst.setText(kuyruk.printUst());                 //Her 2 sn de bir üst area güncellenecek...
                        jtxtAreaVezneAnlik.setText("[(" + kuyruk.anlikNode().oncelik + ") (" + kuyruk.anlikNode().getAd_soyad() + " - " + kuyruk.anlikNode().sure + ")]");

                        if (kuyruk.anlikNode().oncelik == 0) {
                            istatistik_0 += kuyruk.anlikNode().ad_soyad + ", ";
                        } else if (kuyruk.anlikNode().oncelik == 1) {
                            istatistik_1 += kuyruk.anlikNode().ad_soyad + ", ";
                        } else if (kuyruk.anlikNode().oncelik == 2) {
                            istatistik_2 += kuyruk.anlikNode().ad_soyad + ", ";
                        }

                        if (kuyruk.anlikNode().sure > 40000) {                  //40 sn den fazla ise içeri girip işlemleri yap
                            DosyaYazma("gunlukIslem.txt", kuyruk.anlikNode().ad_soyad, kuyruk.anlikNode().oncelik, 40000);
                            if (kuyruk.anlikNode().oncelik == 0) {
                                toplamSure_0 += 40000;
                            } else if (kuyruk.anlikNode().oncelik == 1) {
                                toplamSure_1 += 40000;
                            } else if (kuyruk.anlikNode().oncelik == 2) {
                                toplamSure_2 += 40000;
                            }

                            jtxtAreaMusteriGecmis.append("[(" + kuyruk.anlikNode().oncelik + ")(" + kuyruk.anlikNode().ad_soyad + "-" + 40000 + ")]- vezneye geldi, 40 sn hizmet aldı ve kuyruğun sonuna eklendi.\n");
                            kuyruk.anlikNode().sure -= 40000;
                            kuyruk.addLast(kuyruk.anlikNode().ad_soyad, kuyruk.anlikNode().sure, kuyruk.anlikNode().oncelik);
                            kuyruk.remove();

                            //kuyruk.print();
                        } else {                                                // 40 sn den az ise içeri girip işlemleri yap.
                            DosyaYazma("gunlukIslem.txt", kuyruk.anlikNode().ad_soyad, kuyruk.anlikNode().oncelik, kuyruk.anlikNode().sure);
                            if (kuyruk.anlikNode().oncelik == 0) {
                                toplamSure_0 += kuyruk.anlikNode().sure;
                            } else if (kuyruk.anlikNode().oncelik == 1) {
                                toplamSure_1 += kuyruk.anlikNode().sure;
                            } else if (kuyruk.anlikNode().oncelik == 2) {
                                toplamSure_2 += kuyruk.anlikNode().sure;
                            }

                            jtxtAreaMusteriGecmis.append("[(" + kuyruk.anlikNode().oncelik + ")(" + kuyruk.anlikNode().ad_soyad + "-" + kuyruk.anlikNode().sure + ")]- vezneye geldi, " + (float) kuyruk.anlikNode().sure / 1000 + " sn hizmet aldı ve vezneden ayrildi.\n");
                            kuyruk.remove();
                        }

                        //-------------------
                        try {
                            TimeUnit.SECONDS.sleep(2);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(menu.class.getName()).log(Level.SEVERE, null, ex);
                        }

                        if (isWorking != true) {
                            break;
                        }

                    }
                    if (kuyruk.anlikNode() == null) {
                        jbtnBankaKapandi.doClick();
                    }

                } catch (InterruptedException e1) {
                    e1.printStackTrace();
                }

            }
        };

        System.out.println("Vezne Calısıyor Butonuna Tiklandi...");

        t.start();
        System.out.println("");

    }//GEN-LAST:event_jbtnVezneCalisiyorActionPerformed

