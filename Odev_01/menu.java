    public static void DosyaOlustur(String URL) {
        File f = new File(URL);
        try {
            if (!f.exists()) {                                                  // eğer dosya yoksa
                f.createNewFile();                                              // dosyayı oluşturur.
                System.out.println(f.getName() + " adlı dosya oluşturuldu...");

            } else {
                System.out.println("Dosya halihazırda var, oluşturma işlemi başarısız...");
            }
        } catch (IOException e) {                                               // hata yakalama
            // TODO: handle exception
            e.printStackTrace();
        }
    }

    public static void DosyaSil(String URL) {
        File f = new File(URL);

        if (f.exists()) {                                                       // eğer dosya varsa
            f.delete();                                                         // dosyamızı siliyoruz.
            System.out.println(f.getName() + " adlı dosya silindi...");
        } else {
            System.out.println("Dosya bulunamadı, silme işlemi başarısız...");
        }
    }

    public static void DosyaYazma(String URL, String ad, int oncelik, int sure) {
        File file = new File(URL);                                              // hangi dosya üzerinde işlem yapacağımızı seçiyoruz.
        ad = ad.replace(" ", "");                                               //BOŞLUKLARI SİLME
        try {
            FileOutputStream fos = new FileOutputStream(file, true);
            BufferedWriter writer = new BufferedWriter(new OutputStreamWriter(System.out));
            writer.newLine();
            String[] words = ad.split(" ");
            for (String word : words) {
            }
            String yazi = ad + "#" + oncelik + "#" + sure + "\n";               // İstediğimiz formattaki yazıyı String yazi'ya atıyoruz.

            fos.write(yazi.getBytes());
            fos.flush();
            fos.close();
        } catch (FileNotFoundException e) {
// TODO Auto-generated catch block
            e.printStackTrace();
        } catch (IOException e) {
// TODO Auto-generated catch block
            e.printStackTrace();
        }
    }

